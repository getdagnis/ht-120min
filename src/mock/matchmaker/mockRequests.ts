import { getMockMatchmakerTeamById } from './mockTeams';

const buildTeamRecord = (teamId: number): NonNullable<MatchmakerRequest['team']> => {
  const team = getMockMatchmakerTeamById(teamId);

  if (!team) {
    throw new Error(`Missing mock team ${teamId}`);
  }

  return {
    name: team.teamName,
    ht_team_id: team.teamId,
    logo_url: null,
    country_name: team.countryName ?? null,
    league_id: null,
    gender_id: team.genderId ?? 1,
    fanclub_size: null,
    arena_id: null,
    arena_size: null,
    arena_image_url: null,
    availabilityStatus: team.availabilityStatus,
    availabilityReason: team.availabilityReason,
  };
};

const buildProfile = (managerName: string, countryName: string, leagueId: number | null) => ({
  manager_name: managerName,
  avatar_json: null,
  country_name: countryName,
  league_id: leagueId,
});

const buildRequest = (
  now: Date,
  teamId: number,
  managerHtId: number,
  overrides: Partial<MatchmakerRequest>,
  createdAgoHours: number,
): MatchmakerRequest => ({
  id: `mock-request-${teamId}`,
  team_id: `mock-team-${teamId}`,
  manager_ht_id: managerHtId,
  match_type: '120min',
  opponent_location: 'any',
  home_away: 'any',
  match_day: 'Wednesday',
  time_window: null,
  message: null,
  status: 'open',
  matched_with_team_id: null,
  matched_at: null,
  expires_at: calculateMatchmakerExpiry(now).toISOString(),
  created_at: new Date(now.getTime() - createdAgoHours * 60 * 60 * 1000).toISOString(),
  is_back_and_forth: false,
  is_long_term: false,
  gender_id: 1,
  is_mock: true,
  ...overrides,
  team: overrides.team ?? buildTeamRecord(teamId),
});

